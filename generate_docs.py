class DocPDF(FPDF):
    """Общий базовый класс для всех трёх документов."""

    _header_text = "NLP-Emo-01"

    def header(self):
        if self.page_no() == 1:
            return  # титульная страница без шапки
        self.set_font("DejaVu", "B", 9)
        self.set_text_color(130, 130, 130)
        self.cell(0, 8, self._header_text, align="R",
                  new_x="LMARGIN", new_y="NEXT")
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(4)

    def footer(self):
        self.set_y(-15)
        self.set_font("DejaVu", "", 8)
        self.set_text_color(130, 130, 130)
        self.cell(0, 10, f"Страница {self.page_no()}/{{nb}}", align="C")

    # ---------- вспомогательные методы ----------

    def title_page(self, doc_title, subtitle=None):
        """Титульная страница с названием проекта и документа."""
        self.add_page()
        self.ln(20)
        self.set_font("DejaVu", "", 12)
        self.set_text_color(80, 80, 80)
        self.cell(0, 8, "МГТУ им. Н.Э. Баумана", align="C",
                  new_x="LMARGIN", new_y="NEXT")
        self.cell(0, 7, "Кафедра ИУ10, группа ИУ10-44", align="C",
                  new_x="LMARGIN", new_y="NEXT")
        self.ln(20)
        self.set_font("DejaVu", "B", 22)
        self.set_text_color(30, 60, 120)
        self.cell(0, 14, "NLP-Emo-01", align="C",
                  new_x="LMARGIN", new_y="NEXT")
        self.ln(2)
        self.set_font("DejaVu", "", 14)
        self.set_text_color(60, 60, 60)
        self.cell(0, 10, "Распознавание эмоций по тексту", align="C",
                  new_x="LMARGIN", new_y="NEXT")
        self.ln(6)
        self.set_draw_color(30, 60, 120)
        self.line(50, self.get_y(), 160, self.get_y())
        self.ln(10)
        self.set_font("DejaVu", "B", 15)
        self.set_text_color(30, 60, 120)
        self.cell(0, 10, doc_title, align="C",
                  new_x="LMARGIN", new_y="NEXT")
        if subtitle:
            self.ln(2)
            self.set_font("DejaVu", "", 11)
            self.set_text_color(100, 100, 100)
            self.cell(0, 8, subtitle, align="C",
                      new_x="LMARGIN", new_y="NEXT")
        self.ln(30)
        self.set_font("DejaVu", "", 11)
        self.set_text_color(60, 60, 60)
        self.cell(0, 7, "Исполнители: Осипова П.С., Селина М.А.", align="C",
                  new_x="LMARGIN", new_y="NEXT")
        self.cell(0, 7, "Руководитель: Буркацкий К.А.", align="C",
                  new_x="LMARGIN", new_y="NEXT")
        self.ln(8)
        self.cell(0, 7, "Москва, 2026", align="C",
                  new_x="LMARGIN", new_y="NEXT")

    def section(self, title):
        self.ln(4)
        self.set_font("DejaVu", "B", 14)
        self.set_text_color(30, 60, 120)
        self.cell(0, 10, title, new_x="LMARGIN", new_y="NEXT")
        self.set_draw_color(30, 60, 120)
        self.line(10, self.get_y(), 200, self.get_y())
        self.ln(3)

    def subsection(self, title):
        self.ln(2)
        self.set_font("DejaVu", "B", 11)
        self.set_text_color(50, 50, 50)
        self.cell(0, 8, title, new_x="LMARGIN", new_y="NEXT")
        self.ln(1)

    def body(self, text):
        self.set_font("DejaVu", "", 10)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 6, text)
        self.ln(1)

    def bullet(self, text, indent=10):
        self.set_font("DejaVu", "", 10)
        self.set_text_color(30, 30, 30)
        self.cell(indent, 6, "  \u2022", new_x="END")
        self.multi_cell(0, 6, text, new_x="LMARGIN", new_y="NEXT")

    def bold_bullet(self, bold_part, rest, indent=10):
        self.set_text_color(30, 30, 30)
        self.set_font("DejaVu", "", 10)
        self.cell(indent, 6, "  \u2022", new_x="END")
        self.set_font("DejaVu", "B", 10)
        w = self.get_string_width(bold_part)
        self.cell(w, 6, bold_part, new_x="END")
        self.set_font("DejaVu", "", 10)
        self.multi_cell(0, 6, rest, new_x="LMARGIN", new_y="NEXT")

    def num_bullet(self, num, text):
        self.set_font("DejaVu", "B", 10)
        self.set_text_color(30, 60, 120)
        self.cell(10, 6, f"  {num}.", new_x="END")
        self.set_font("DejaVu", "", 10)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 6, text, new_x="LMARGIN", new_y="NEXT")

    def code_block(self, text):
        self.set_font("DejaVuMono", "", 9)
        self.set_fill_color(240, 240, 240)
        self.set_text_color(30, 30, 30)
        self.set_draw_color(200, 200, 200)
        lines = text.strip().split("\n")
        h = len(lines) * 5.5 + 6
        y = self.get_y()
        if y + h > 270:
            self.add_page()
            y = self.get_y()
        self.rect(12, y, 186, h, "DF")
        self.ln(3)
        for line in lines:
            self.cell(5)
            self.cell(0, 5.5, "  " + line, new_x="LMARGIN", new_y="NEXT")
        self.ln(4)

    def table_row(self, cells, widths, header=False):
        self.set_font("DejaVu", "B" if header else "", 9)
        if header:
            self.set_fill_color(30, 60, 120)
            self.set_text_color(255, 255, 255)
        else:
            self.set_fill_color(248, 248, 248)
            self.set_text_color(30, 30, 30)
        h = 8
        for i, cell in enumerate(cells):
            self.cell(widths[i], h, " " + cell, border=1, fill=True)
        self.ln(h)

    def table_row_multi(self, cells, widths, header=False):
        """Строка таблицы с переносом текста в каждой ячейке."""
        self.set_font("DejaVu", "B" if header else "", 8)
        if header:
            self.set_fill_color(30, 60, 120)
            self.set_text_color(255, 255, 255)
        else:
            self.set_fill_color(248, 248, 248)
            self.set_text_color(30, 30, 30)
        line_h = 5
        x_start = self.get_x()
        y_start = self.get_y()
        max_h = 0
        # Первый проход: вычисляем высоту каждой ячейки
        cell_heights = []
        for i, cell in enumerate(cells):
            self.set_xy(x_start + sum(widths[:i]), y_start)
            # Считаем количество строк
            nb_lines = 1
            if widths[i] > 0:
                # Приблизительный расчёт
                text_w = self.get_string_width(cell) + 2
                if text_w > widths[i] - 2:
                    nb_lines = int(text_w / (widths[i] - 4)) + 1
            ch = max(nb_lines * line_h, line_h)
            cell_heights.append(ch)
            if ch > max_h:
                max_h = ch
        max_h = max(max_h, line_h + 2)
        # Проверка на конец страницы
        if y_start + max_h > 270:
            self.add_page()
            y_start = self.get_y()
        # Второй проход: рисуем ячейки
        for i, cell in enumerate(cells):
            x = x_start + sum(widths[:i])
            self.rect(x, y_start, widths[i], max_h, "DF")
            self.set_draw_color(200, 200, 200)
            self.rect(x, y_start, widths[i], max_h, "D")
            self.set_xy(x + 1, y_start + 1)
            self.multi_cell(widths[i] - 2, line_h, cell)
        self.set_xy(x_start, y_start + max_h)


def _init_pdf(header_text):
    """Создать и настроить экземпляр DocPDF."""
    pdf = DocPDF()
    pdf._header_text = header_text
    pdf.alias_nb_pages()
    pdf.set_auto_page_break(auto=True, margin=20)

    reg, bold, mono = find_fonts()
    pdf.add_font("DejaVu", "", reg)
    pdf.add_font("DejaVu", "B", bold)
    pdf.add_font("DejaVuMono", "", mono)
    return pdf


